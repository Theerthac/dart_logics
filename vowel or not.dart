void main(List<String> args) {
  String character = 'e';
  character.toLowerCase();
  if (character == 'a' ||
      character == 'e' ||
      character == 'i' ||
      character == 'o' ||
      character == 'u') {
    print('The Character is vowel $character');
  } else {
    print('The character is not vowel');
  }
}
